    updated_at     DATETIME(6)  NOT NULL                COMMENT '수정 시각(마이크로초, 개편 시 재적재 runbook에서 갱신)',
    PRIMARY KEY (id),
    -- Q1(시군구 코드 단건 조회)·Q3(UNSPECIFIED 폴백 조회) 지원 + 중복 seed 방지(멱등성 보장, 시군구 코드는 전역 유일)
    UNIQUE KEY uk_regions_sigungu_code (sigungu_code) COMMENT '시군구 코드 전역 유일 — 단건 조회 UK + 재적재 시 중복 방지',
    -- Q2(시도별 시군구 목록 조회, 관리/검증용). 선두 카디널리티는 낮으나(17종) 단독 조건 조회라 유효.
    INDEX idx_regions_sido_code (sido_code) COMMENT '시도별 목록 조회(Q2)',
    -- Q4(주소 파서의 명칭 텍스트 매칭: "부산광역시 해운대구" → 코드). 컬럼 순서: 파서 입력 순서(시도→시군구)와
    -- 일치시키고, 시도명(카디널리티 17)으로 먼저 좁혀 동명 시군구(중구·남구·동구 등) 충돌을 시도명이 분리한다.
    INDEX idx_regions_sido_name_sigungu_name (sido_name, sigungu_name) COMMENT '주소 파서 명칭 역조회(Q4)'
) COMMENT '시도·시군구 표준코드 마스터 (법정동코드 기준, 정적 참조 — 소프트삭제/audit 컬럼 미포함, seed 주체는 Flyway 단일)';

