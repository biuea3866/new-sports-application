    sigungu_code   VARCHAR(5)   NOT NULL                COMMENT '시군구 표준코드(법정동코드 앞5자리), UNSPECIFIED=00000',
    sigungu_name   VARCHAR(60)  NOT NULL                COMMENT '시군구명, UNSPECIFIED=미지정',
